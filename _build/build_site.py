def write(path_rel, html):
    full = os.path.join(ROOT, path_rel)
    os.makedirs(os.path.dirname(full), exist_ok=True)
    with open(full, "w", encoding="utf-8") as f:
        f.write(html)
    print("wrote", path_rel)


def generate_lang_pages(prefix):
    write(f"{prefix}index.html", build_home())
    write(f"{prefix}tsiny/index.html", build_tsiny())
    for s in SERVICES:
        write(f"{prefix}{s['slug']}/index.html", build_service_page(s["slug"]))
    write(f"{prefix}pro-nas/index.html", build_pro_nas())
    write(f"{prefix}vidhuky/index.html", build_vidhuky())
    write(f"{prefix}kontakty/index.html", build_kontakty())
    write(f"{prefix}rayony/index.html", build_rayony_index())
    for i, area in enumerate(AREAS):
        write(f"{prefix}rayony/{area['slug']}/index.html", build_rayon_page(area, i))


def logical_urls():
    return (["/", "/tsiny/"] + [f"/{s['slug']}/" for s in SERVICES] + ["/pro-nas/", "/vidhuky/", "/kontakty/"]
            + ["/rayony/"] + [f"/rayony/{a['slug']}/" for a in AREAS])


def main():
    global LANG

    LANG = "uk"
    generate_lang_pages("")

    LANG = "ru"
    generate_lang_pages("ru/")

    robots = f"""User-agent: *
Allow: /

Sitemap: https://{DOMAIN}/sitemap.xml
"""
    write("robots.txt", robots)

    urls = logical_urls()
    entries = [f"https://{DOMAIN}{u}" for u in urls] + [f"https://{DOMAIN}{to_ru_path(u)}" for u in urls]
    urlset = "\n".join(f"  <url><loc>{u}</loc></url>" for u in entries)
    sitemap = f"""<?xml version="1.0" encoding="UTF-8"?>
<urlset xmlns="http://www.sitemaps.org/schemas/sitemap/0.9">
{urlset}
</urlset>
"""
    write("sitemap.xml", sitemap)


if __name__ == "__main__":
    main()
