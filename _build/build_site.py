def slugify_ua(text):
    text = text.lower()
    text = re.sub(r"\([^)]*\)", "", text)  # drop parenthetical part, e.g. "Центр (Хрещатик)"
    out = []
    for ch in text:
        if ch in _TRANSLIT:
            out.append(_TRANSLIT[ch])
        elif ch.isalnum():
            out.append(ch)
        else:
            out.append("-")
    slug = "".join(out)
    slug = re.sub(r"-+", "-", slug).strip("-")
    return slug


