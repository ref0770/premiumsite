def breadcrumbs(items):
    # items: list of (label, path_or_None) — path is a logical (unprefixed) path
    parts = []
    for label, path in items:
        if path:
            parts.append(f'<a href="{href(path)}">{label}</a>')
        else:
            parts.append(f"<span>{label}</span>")
    return '<nav class="breadcrumbs">' + " <span>/</span> ".join(parts) + "</nav>"


